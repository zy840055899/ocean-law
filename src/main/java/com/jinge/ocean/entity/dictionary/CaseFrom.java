package com.jinge.ocean.entity.dictionary;

/**
 * 案件来源实体类
 * @author zheng 2018-9-28 10:11:40
 */
public class CaseFrom {
    private int id;
    private String caseFrom;
    private String remark;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCaseFrom() {
        return caseFrom;
    }

    public void setCaseFrom(String caseFrom) {
        this.caseFrom = caseFrom;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }
}
