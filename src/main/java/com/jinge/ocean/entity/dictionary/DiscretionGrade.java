package com.jinge.ocean.entity.dictionary;

/**
 * 裁量阶次
 *
 * @author zheng 2018-9-28 14:39:55
 */
public class DiscretionGrade {
    private int id;
    private String discretionGrade;
    private String remark;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDiscretionGrade() {
        return discretionGrade;
    }

    public void setDiscretionGrade(String discretionGrade) {
        this.discretionGrade = discretionGrade;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }
}
